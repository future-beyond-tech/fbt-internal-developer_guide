// ── Computed stats ──────────────────────────────────────────
export function getMapStats() {
  const totalTechniques = techniqueDomains.reduce((sum, d) => sum + d.items.length, 0);
  const coveredTechniques = techniqueDomains.reduce(
    (sum, d) => sum + d.items.filter(i => i.deepRefId !== null).length, 0
  );
  const gapTechniques = totalTechniques - coveredTechniques;
  return {
    domains: techniqueDomains.length,
    totalTechniques,
    coveredTechniques,
    gapTechniques,
    coveragePercent: Math.round((coveredTechniques / totalTechniques) * 100),
  };
}

// ── Gap analysis helper ─────────────────────────────────────
export function getGapsByDomain() {
  return techniqueDomains.map(domain => ({
    domain: domain.label,
    domainId: domain.id,
    icon: domain.icon,
    total: domain.items.length,
    covered: domain.items.filter(i => i.deepRefId !== null).length,
    gaps: domain.items.filter(i => i.deepRefId === null).map(i => i.name),
  })).filter(d => d.gaps.length > 0);
}
